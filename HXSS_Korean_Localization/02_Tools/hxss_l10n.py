SUBSTITUTIONS = ""


@dataclass(frozen=True)
class Entry:
    source: str
    target: str
    line: str
    line_no: int
    path: Path


def read_lines(path: Path) -> list[str]:
    if not path.exists():
        return []
    return path.read_text(encoding="utf-8-sig", errors="replace").splitlines()


def write_lines(path: Path, lines: list[str]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    payload = "\n".join(lines)
    if payload:
        payload += "\n"
    path.write_text(payload, encoding="utf-8")


def parse_entries(path: Path) -> tuple[list[Entry], list[tuple[int, str]]]:
    entries: list[Entry] = []
    bad: list[tuple[int, str]] = []
    for line_no, line in enumerate(read_lines(path), 1):
        if not line.strip():
            continue
        split_at = find_unescaped_equals(line)
        if split_at < 0:
            bad.append((line_no, line))
            continue
        source, target = line[:split_at], line[split_at + 1 :]
        entries.append(Entry(source, target, line, line_no, path))
    return entries, bad


def find_unescaped_equals(line: str) -> int:
    for index, char in enumerate(line):
        if char == "=" and (index == 0 or line[index - 1] != "\\"):
            return index
    return -1


def format_entry(source: str, target: str) -> str:
    return f"{source}={target}"


def manual_paths() -> list[Path]:
    return [TEXT_DIR / name for name in TEXT_FILES.values()]


def ready_paths() -> list[Path]:
    if not WORK_DIR.exists():
        return []
    return sorted(WORK_DIR.glob("Ready_*.txt"))


def ensure_structure() -> None:
    TEXT_DIR.mkdir(parents=True, exist_ok=True)
    WORK_DIR.mkdir(parents=True, exist_ok=True)

    for filename in TEXT_FILES.values():
        path = TEXT_DIR / filename
        if not path.exists():
            path.write_text("", encoding="utf-8")

    for filename in WORK_FILES:
        path = WORK_DIR / filename
        if not path.exists():
            if filename == "Glossary.md":
                path.write_text(GLOSSARY, encoding="utf-8")
            else:
                path.write_text("", encoding="utf-8")

    for filename in ("_Preprocessors.txt", "_Postprocessors.txt"):
        path = TEXT_DIR / filename
        if not path.exists():
            path.write_text("", encoding="utf-8")

    subst = TEXT_DIR / "_Substitutions.txt"
    if not subst.exists() or not subst.read_text(encoding="utf-8-sig", errors="replace").strip():
        subst.write_text(SUBSTITUTIONS, encoding="utf-8")


def applied_sources() -> set[str]:
    sources: set[str] = set()
    for path in manual_paths():
        entries, _ = parse_entries(path)
        sources.update(entry.source for entry in entries)
    return sources


def scan() -> None:
    ensure_structure()
    auto_entries, bad = parse_entries(AUTO_FILE)
    applied = applied_sources()

    seen: set[str] = set()
    new_lines: list[str] = []
    for entry in auto_entries:
        if entry.source in applied or entry.source in seen:
            continue
        seen.add(entry.source)
        new_lines.append(entry.line)

    write_lines(WORK_DIR / "Unsorted_New.txt", new_lines)

    if bad:
        write_lines(
            WORK_DIR / "Validation_Report.txt",
            [f"[AutoGenerated no '='] line {line_no}: {line}" for line_no, line in bad],
        )

    print(f"scan: {len(new_lines)} new entries written to {WORK_DIR / 'Unsorted_New.txt'}")


def contains_any(text: str, words: tuple[str, ...]) -> bool:
    return any(word in text for word in words)


