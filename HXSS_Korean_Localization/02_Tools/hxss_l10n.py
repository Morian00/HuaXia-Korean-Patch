def classify() -> None:
    ensure_structure()
    source_path = WORK_DIR / "Unsorted_New.txt"
    entries, bad = parse_entries(source_path)

    buckets: dict[str, list[str]] = {key: [] for key in CANDIDATE_FILES}
    for entry in entries:
        buckets[classify_source(entry.source)].append(entry.line)

    for key, filename in CANDIDATE_FILES.items():
        write_lines(WORK_DIR / filename, buckets[key])

    if bad:
        report = [f"[Unsorted no '='] line {line_no}: {line}" for line_no, line in bad]
        write_lines(WORK_DIR / "Validation_Report.txt", report)

    counts = ", ".join(f"{key}:{len(lines)}" for key, lines in sorted(buckets.items()))
    print(f"classify: {counts}")


def target_for_ready(path: Path) -> Path | None:
    name = path.name
    match = re.match(r"Ready_(\d{2})_", name)
    if not match:
        return None
    key = match.group(1)
    filename = TEXT_FILES.get(key)
    return TEXT_DIR / filename if filename else None


def apply_ready() -> None:
    ensure_structure()
    report: list[str] = []
    total_added = 0
    total_updated = 0
    all_manual_sources = applied_sources()

    for ready in ready_paths():
        target_path = target_for_ready(ready)
        if target_path is None:
            report.append(f"[Apply skipped] Unknown Ready target: {ready.name}")
            continue

        ready_entries, bad = parse_entries(ready)
        for line_no, line in bad:
            report.append(f"[Apply skipped] {ready.name}:{line_no} has no '=': {line}")

        existing_entries, _ = parse_entries(target_path)
        existing_by_source = {entry.source: entry.target for entry in existing_entries}

        for entry in ready_entries:
            issues = validate_entry(entry)
            if issues:
                report.extend(f"[Apply skipped] {ready.name}:{entry.line_no}: {issue}" for issue in issues)
                continue

            is_override = target_path.name == TEXT_FILES["99"]
            if entry.source in all_manual_sources and entry.source not in existing_by_source and not is_override:
                report.append(
                    f"[Apply skipped] {ready.name}:{entry.line_no}: duplicate source already exists in another manual file: {entry.source}"
                )
                continue

            if entry.source in existing_by_source:
                existing_by_source[entry.source] = entry.target
                total_updated += 1
            else:
                existing_by_source[entry.source] = entry.target
                all_manual_sources.add(entry.source)
                total_added += 1

        merged = [format_entry(source, target) for source, target in existing_by_source.items()]
        write_lines(target_path, merged)

    if report:
        write_lines(WORK_DIR / "Validation_Report.txt", report)

    print(f"apply: added={total_added}, updated={total_updated}, warnings={len(report)}")


TAG_RE = re.compile(r"</?(?:color|size|align|margin|link|b|i)(?:\\?=[^>]*)?>", re.IGNORECASE)
VAR_RE = re.compile(r"\{\{[^{}]+\}\}|\{\d+\}|%[sdif]")
NUMBER_RE = re.compile(r"[+-]?\d+(?:\.\d+)?%?")

ALLOW_EXTRA_TARGET_NEWLINES = {
    "全部随机",
    "重置头部",
    "随机头部",
    "重置头发",
    "随机头发",
    "重置胡须",
    "随机胡须",
    "重置眉毛",
    "随机眉毛",
    "重置眼睛",
    "随机眼睛",
    "重置身形",
    "随机身形",
    "重置皮肤",
    "随机皮肤",
    "重置声音",
    "随机声音",
}


def multiset(values: list[str]) -> Counter[str]:
    return Counter(values)


def validate_entry(entry: Entry) -> list[str]:
    issues: list[str] = []
    source = entry.source
    target = entry.target

    if not source:
        issues.append("empty source")
    if not target:
        issues.append("empty translation")

    if source.count(r"\n") != target.count(r"\n") and source not in ALLOW_EXTRA_TARGET_NEWLINES:
        issues.append(f"literal \\n count mismatch source={source.count(r'\\n')} target={target.count(r'\\n')}")

    source_tags = TAG_RE.findall(source)
    target_tags = TAG_RE.findall(target)
    if multiset(source_tags) != multiset(target_tags):
        issues.append("rich text tag mismatch")

    source_vars = VAR_RE.findall(source)
    target_vars = VAR_RE.findall(target)
    if multiset(source_vars) != multiset(target_vars):
        issues.append("variable placeholder mismatch")

    source_numbers = NUMBER_RE.findall(source)
    target_numbers = NUMBER_RE.findall(target)
    if multiset(source_numbers) != multiset(target_numbers):
        issues.append("number/percent token mismatch")

    return issues


def validate_files() -> None:
    ensure_structure()
    report: list[str] = []
    seen: dict[str, Path] = {}

    paths = manual_paths() + ready_paths()
    for path in paths:
        entries, bad = parse_entries(path)
        for line_no, line in bad:
            report.append(f"[No '='] {path.relative_to(BASE_DIR)}:{line_no}: {line}")
        for entry in entries:
            for issue in validate_entry(entry):
                report.append(f"[{issue}] {path.relative_to(BASE_DIR)}:{entry.line_no}: {entry.line}")

            if path in manual_paths():
                previous = seen.get(entry.source)
                is_allowed_override = previous is not None and (
                    path.name == TEXT_FILES["99"] or previous.name == TEXT_FILES["99"]
                )
                if previous is not None and not is_allowed_override:
                    report.append(
                        f"[duplicate source] {path.relative_to(BASE_DIR)}:{entry.line_no} duplicates {previous.relative_to(BASE_DIR)}: {entry.source}"
                    )
                else:
                    seen[entry.source] = path

    write_lines(WORK_DIR / "Validation_Report.txt", report)
    print(f"validate: {len(report)} issue(s), report={WORK_DIR / 'Validation_Report.txt'}")


def count_entries(path: Path) -> int:
    entries, _ = parse_entries(path)
    return len(entries)


def report() -> None:
    ensure_structure()
    auto_entries, auto_bad = parse_entries(AUTO_FILE)
    auto_unique = len({entry.source for entry in auto_entries})
    applied = applied_sources()
    unsorted = count_entries(WORK_DIR / "Unsorted_New.txt")
    validation_issues = len([line for line in read_lines(WORK_DIR / "Validation_Report.txt") if line.strip()])

    print("HXSS localization report")
    print(f"auto entries: {len(auto_entries)}")
    print(f"auto unique sources: {auto_unique}")
    print(f"auto malformed lines: {len(auto_bad)}")
    print(f"manual applied sources: {len(applied)}")
    print(f"unsorted new entries: {unsorted}")

    print("manual files:")
    for key, filename in TEXT_FILES.items():
        print(f"  {key} {filename}: {count_entries(TEXT_DIR / filename)}")

    print("candidate files:")
    for key, filename in CANDIDATE_FILES.items():
        print(f"  {key} {filename}: {count_entries(WORK_DIR / filename)}")

    print(f"validation issues: {validation_issues}")
    print(f"task file: {TASK_FILE}")


def main() -> None:
    parser = argparse.ArgumentParser(description="Huaxia: Warring States localization workflow helper")
    parser.add_argument("command", choices=["init", "scan", "classify", "apply", "validate", "report"])
    args = parser.parse_args()

    if args.command == "init":
        ensure_structure()
        print(f"init: structure ready under {BASE_DIR}")
    elif args.command == "scan":
        scan()
    elif args.command == "classify":
        classify()
    elif args.command == "apply":
        apply_ready()
    elif args.command == "validate":
        validate_files()
    elif args.command == "report":
        report()


if __name__ == "__main__":
    main()
